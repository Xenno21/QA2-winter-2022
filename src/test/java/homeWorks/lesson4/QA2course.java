package homeWorks.lesson4;

public class QA2course {
    private String name;
    private Integer pupilsNumber;
    private Integer lessonNumber;
    private Long lessonTimeSec;
    private Character lessonLanguage;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getPupilsNumber() {
        return pupilsNumber;
    }

    public void setPupilsNumber(Integer pupilsNumber) {
        this.pupilsNumber = pupilsNumber;
    }

    public Integer getLessonNumber() {
        return lessonNumber;
    }

    public void setLessonNumber(Integer lessonNumber) {
        this.lessonNumber = lessonNumber;
    }

    public Long getLessonTimeSec() {
        return lessonTimeSec;
    }

    public void setLessonTimeSec(Long lessonTimeSec) {
        this.lessonTimeSec = lessonTimeSec;
    }

    public Character getLessonLanguage() {
        return lessonLanguage;
    }

    public void setLessonLanguage(Character lessonLanguage) {
        this.lessonLanguage = lessonLanguage;
    }
}

